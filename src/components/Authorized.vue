<script>
import { check } from "@/utils/auth";
export default {
  functional: true,
  props: {
    authority: {
      type: Array,
      required: true
    }
  },
  render: (h, content) => {
    const { props, scopedSlots } = content;
    return check(props.authority) ? scopedSlots.default() : null;
  }
};
</script>
